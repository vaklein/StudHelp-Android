public class Course {
    int ID;
    String code;
    String teacher;
    String name;
    String university;

    public Course(int ID, String code, String teacher,String name,String university) {
        this.ID = ID;
        this.code = code;
        this.teacher = teacher;
        this.name = name;
        this.university = university;
    }

}
